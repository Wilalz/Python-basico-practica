# Operadores
n1 = 10
n2 = 15


## De Comparacion
print(n1 < n2)
print(n1 > n2)
print(n1 <= n2)
print(n1 >= n2)
print(n1 == n2)
print(n1 == 10)


## Logicos   -  and, or, not
print('--- Operadores logicos ---')
print(n1 > 5 and n2 == 15)  # ambas se tienen que cumplir para ser True
print(n1 > 5 or n2 == 15)  # alguna se debe cumplir para ser True
print(not n2 == 15)   # niega el resultado
